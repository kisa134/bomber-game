export const FACTION_LIST: Faction[] = Object.values(FACTIONS);

/** Получить фракцию по ID */
export function getFaction(id: FactionId): Faction {
  return FACTIONS[id];
}

/** Получить все враги фракции */
export function getFactionEnemies(id: FactionId): Faction[] {
  return FACTIONS[id].enemies.map((enemyId) => FACTIONS[enemyId]);
}

/** Получить всех союзников фракции */
export function getFactionAllies(id: FactionId): Faction[] {
  return FACTIONS[id].allies.map((allyId) => FACTIONS[allyId]);
}

/** Получить фракцию по worldId */
export function getFactionByWorldId(worldId: string): Faction | undefined {
  return FACTION_LIST.find((f) => f.worldId === worldId);
}
